package org.lab1.json;

public class InAppAddJson {

    private int monetizedApplicationId;
    private String title;
    private String description;
    private double price;

    public int getMonetizedApplicationId() {
        return monetizedApplicationId;
    }

    public void setMonetizedApplicationId(int monetizedApplicationId) {
        this.monetizedApplicationId = monetizedApplicationId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
